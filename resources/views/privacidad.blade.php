<h1>(LEY DE PROTECCIÓN DE DATOS PERSONALES EN POSESIÓN DE SUJETOS OBLIGADOS PARA EL ESTADO DE BAJA CALIFORNIA SUR)</h1>

<p>Aviso de privacidad simplificado para el proceso del Programa de “CREDENCIALIZACIÓN DE ESTUDIANTES DEL MUNICIPIO DE LA PAZ” 
de la Dirección de Movilidad y Transporte, de la Dirección General de Seguridad Pública, Policía Preventiva y Tránsito Municipal del H. XVII Ayuntamiento de La Paz, 
Baja California Sur.</p>   

<h3>Denominación y fundamento legal de la responsable</h3>
<p>La Dirección General de Seguridad Pública, Policía Preventiva y Tránsito Municipal del H. XVII Ayuntamiento de La Paz, por conducto de la Dirección de Movilidad y 
Transporte, con fundamento en lo dispuesto por el artículo 13 apartado B fracciones III, IV y VIII primer párrafo de la Constitución Política del Estado Libre y 
Soberano de Baja California Sur; 51 fracción III incisos a) y d), 103 fracción IV, 132 fracción IX  y 143 de la Ley Orgánica del Gobierno Municipal del Estado de Baja
California Sur; 1 párrafo cuarto, 2 fracciones II, III, IV y V, 3 fracciones II, VIII y XIII y 5 fracción I de la Ley de Protección de  Datos Personales en Posesión
de Sujetos Obligados para el Estado de Baja California Sur; 3, 6 fracción II y 8 fracción IV de la Ley de Transporte para el Estado de Baja California Sur,13 fracción
VIII, 134  fracción I y IV, 135 fracción I; 137 fracción I, II y IX del Reglamento de la Administración Pública Municipal del Ayuntamiento de La Paz, Baja California Sur;
siendo esta Dependencia la responsable del tratamiento de los datos personales que nos proporcionen.</p>  

<p>Finalidad del tratamiento de datos personales<br>
La finalidad del tratamiento de sus datos personales es exclusivamente para el programa de “CREDENCIALIZACION DE ESTUDIANTES PARA SU ACCESO AL DESCUENTO DEL TRANSPORTE 
EN SU CALIDAD DE ESTUDIANTES EN EL MUNICIPIO DE LA PAZ”.</p>  

<p>Transmisión de datos personales<br>
Se hace de su conocimiento que sus datos personales son tratados en los términos establecidos en el artículo 14, 15, 16, 68, 69, 70, 71, 72 y 73 de la Ley de Protección 
de Datos Personales en Posesión de Sujetos Obligados para el Estado de Baja California Sur, y podrán ser transmitidos a otras autoridades siempre y cuando, éstos se utilicen 
para el ejercicio de sus facultades, además de otras transmisiones previstas en la Ley.</p>  

<p>Mecanismos y medios disponibles para manifestar negativa para el tratamiento de datos personales para finalidades y transferencias de datos personales que requieren el 
consentimiento del titular.Si desea manifestar su negativa para el tratamiento de datos personales para finalidades y transferencias de datos personales que requieren el 
consentimiento del titular, es necesario que dicha manifestación se realice por escrito.</p> 

<p>Consulta del aviso de privacidad integral
El aviso de privacidad integral podrá ser consultado en la página institucional de internet del H. XVII Ayuntamiento de La Paz, B.C.S: https://lapaz.gob.mx/.
</p> 

